class BroadcastTo(TensorOp):
    def __init__(self, shape):
        self.shape = shape

    def compute(self, a):
        return array_api.broadcast_to(a, self.shape)

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        input_shape = node.inputs[0].shape
        base_shape = [1] * (len(self.shape) - len(input_shape)) + list(input_shape)
        axes = []
        for i in range(len(base_shape)):
            if self.shape[i] != base_shape[i]:
                axes.append(i)
        out_grad = summation(out_grad, axes=tuple(axes))
        return reshape(out_grad, input_shape)
        ### END YOUR SOLUTION


def broadcast_to(a, shape):
    return BroadcastTo(shape)(a)


class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.sum(a, self.axes)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        input_shape = node.inputs[0].shape
        base_shape = list(input_shape)
        axes = list(range(len(base_shape))) if self.axes == None else self.axes
        if isinstance(axes, int):
            base_shape[axes] = 1
        else:
            for ax in axes:
                base_shape[ax] = 1
        tmp_grad = reshape(out_grad, base_shape)
        out_grad = broadcast_to(tmp_grad, input_shape)
        return out_grad
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        return a @ b
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a, b = node.inputs
        a_shape = a.shape
        b_shape = b.shape
        lhs = out_grad @ transpose(b, [-2, -1])
        rhs = transpose(a, [-2, -1]) @ out_grad
        while a_shape != lhs.shape:
            lhs = summation(lhs, 0)
        while b_shape != rhs.shape:
            rhs = summation(rhs, 0)
        return (lhs, rhs)
        ### END YOUR SOLUTION


def matmul(a, b):
    return MatMul()(a, b)


